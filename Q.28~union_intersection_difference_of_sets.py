# create a code that define two sets of integers. Then, print the union ,intersection ,and difference of these two sets.

set1 = {1,2,3,4,5}
set2 = {4,5,6,7,8}

union_set = set1.union(set2)
intersection_set = set1.intersection(set2)
difference_set = set1.difference(set2)

print("the union of the two sets is :",union_set)
print("the intersection of the two set is :",intersection_set)
print("the difference of the two set is :",difference_set)